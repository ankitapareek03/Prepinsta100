//Check if a Number is Positive or Negative in Java

public class Main 
{
	  public static void main (String[]args)
	  {

	    int num = 5;
	    //int num = -5;
	    //int num = 0;

	    if (num > 0)
	    {
	        System.out.println ("The number is positive");
	    }
	    else if (num < 0)
	    {
	        System.out.println ("The number is negative");
	    }
	    else
	    {
	        System.out.println ("Zero");
	    }
	  }
}
